    public void listarTabela(){
        DefaultTableModel modelo = (DefaultTableModel) tabelaPesquisa.getModel();
        
        ClienteDAO cdao = new ClienteDAO();
        
        cdao.listar().forEach((c) -> {
            modelo.addRow(new Object[]{
                c.getId(),
                c.getCpf(),
                c.getNome(),
                c.getTelefone(),
                c.getEndereco(),
                c.getCidade(),
                c.getEstado()
                    
            });
        });
        
    
    
    
    
    }

